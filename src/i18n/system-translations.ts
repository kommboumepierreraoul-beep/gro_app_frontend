const restoreText = new WeakMap<Node, string>();
const restoreAttributes = new WeakMap<Element, Map<string, string>>();

const normalize = (value: string) => value.replace(/\s+/g, " ").trim();

const repairMojibake = (value: string) => {
  if (!/[ÃÂâ]/.test(value)) return value;

  try {
    const bytes = Uint8Array.from(Array.from(value), (char) =>
      char.charCodeAt(0),
    );
    return new TextDecoder("utf-8").decode(bytes);
  } catch {
    return value;
  }
};

const comparable = (value: string) =>
  normalize(repairMojibake(value))
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase();

const comparableTranslations = new Map<string, string>();

for (const [source, target] of Object.entries(exactTranslations)) {
  comparableTranslations.set(comparable(source), target);
  comparableTranslations.set(comparable(repairMojibake(source)), target);
}

export const shouldSkipTranslation = (element: Element | null) => {
  return Boolean(
    element?.closest(
      "script, style, code, pre, textarea, [contenteditable='true'], [data-no-translate]",
    ),
  );
};

const shouldSkipAttributeTranslation = (element: Element | null) => {
  return Boolean(
    element?.closest("script, style, code, pre, [data-no-translate]"),
  );
};

const translateValue = (value: string, locale: AppLocale) => {
  if (locale === "fr") return value;

  const leading = value.match(/^\s*/)?.[0] ?? "";
  const trailing = value.match(/\s*$/)?.[0] ?? "";
  const normalized = normalize(value);
  if (!normalized) return value;

  const exact = exactTranslations[normalized];
  if (exact) return `${leading}${exact}${trailing}`;

  const normalizedExact = comparableTranslations.get(comparable(normalized));
  if (normalizedExact) return `${leading}${normalizedExact}${trailing}`;

  let translated = value;
  for (const [pattern, replacement] of fragmentTranslations) {
    translated = translated.replace(pattern, replacement);
  }

  return translated;
};

export const translateSystemString = (value: string, locale: AppLocale) =>
  translateValue(value, locale);

export const translateTextNode = (node: Text, locale: AppLocale) => {
  const parent = node.parentElement;
  if (shouldSkipTranslation(parent)) return;

  if (!restoreText.has(node)) restoreText.set(node, node.nodeValue ?? "");

  const source = restoreText.get(node) ?? "";
  const translated = translateValue(source, locale);

  if (node.nodeValue !== translated) node.nodeValue = translated;
};

export const translateElementAttributes = (
  element: Element,
  locale: AppLocale,
) => {
  if (shouldSkipAttributeTranslation(element)) return;

  const attributes = [
    "placeholder",
    "title",
    "aria-label",
    "aria-description",
    "aria-valuetext",
    "alt",
    "data-label",
    "data-title",
    "data-tooltip",
    "data-placeholder",
  ];
  const originalAttributes =
    restoreAttributes.get(element) ?? new Map<string, string>();

  for (const attribute of attributes) {
    const value = element.getAttribute(attribute);
    if (!value) continue;

    if (!originalAttributes.has(attribute)) {
      originalAttributes.set(attribute, value);
    }

    const source = originalAttributes.get(attribute) ?? value;
    const translated = translateValue(source, locale);
    if (translated !== value) element.setAttribute(attribute, translated);
  }

  if (
    element instanceof HTMLInputElement &&
    ["button", "submit", "reset"].includes(element.type) &&
    element.value
  ) {
    if (!originalAttributes.has("value")) {
      originalAttributes.set("value", element.value);
    }

    const source = originalAttributes.get("value") ?? element.value;
    const translated = translateValue(source, locale);
    if (translated !== element.value) element.value = translated;
  }

  if (originalAttributes.size > 0) {
    restoreAttributes.set(element, originalAttributes);
  }
};
